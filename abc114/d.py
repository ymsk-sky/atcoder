"""
n!(=1*2*3*...*n)の約数のうち約数が75個のものはいくつか
"""
n=int(input())
def d(n):
    a,b,i=0,0,1
    while i*i<=n:
        if n%i==0:
            a+=1
            if i!=n//i:
                b+=1
        i+=1
    return a+b
l=[]
for i in range(1,n+1):
    l.append(d(i))
print(l)
